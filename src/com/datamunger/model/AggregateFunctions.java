package com.datamunger.model;

public class AggregateFunctions 
{
	private String sumColumn;
	private String countColumn;
	private String minColumn;
	private String maxColumn;
	private String avgColumn;
	
	public String getSumColumn() {
		return sumColumn;
	}
	public void setSumColumn(String sumColumn) {
		this.sumColumn = sumColumn;
	}
	public String getCountColumn() {
		return countColumn;
	}
	public void setCountColumn(String countColumn) {
		this.countColumn = countColumn;
	}
	public String getMinColumn() {
		return minColumn;
	}
	public void setMinColumn(String minColumn) {
		this.minColumn = minColumn;
	}
	public String getMaxColumn() {
		return maxColumn;
	}
	public void setMaxColumn(String maxColumn) {
		this.maxColumn = maxColumn;
	}
	public String getAverageColumn() {
		return avgColumn;
	}
	public void setAverageColumn(String averageColumn) {
		this.avgColumn = averageColumn;
	}
	

}
